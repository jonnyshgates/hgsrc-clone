		        <div id="Text1" style="position:relative; float:left; width:940px; height:auto; text-align:left; line-height:16px; padding:20px;">
                <a class="arial13grey">The basic rules of squash are outlined below – ( taken from </a>
                <a class="arial13grey hoverHG2" href="http://www.englandsquashandracketball.com">www.englandsquashandracketball.com</a>
                <a class="arial13grey">&</a>
                <a class="arial13grey hoverHG2" href="http://www.worldsquash.org/">www.worldsquash.org</a>
                <a class="arial13grey"> ).<br /><br /></a>
                <a class="arial14grey"><b>1. Squash equipment</b><br /><br /></a>
                <a class="arial13grey">Squash is played between two players on a squash court each using a standard racket, and a ball approved by England Squash & Racketball.<br /><br /></a>
                <a class="arial14grey"><b>2. Scoring</b><br /><br /></a>
                <a class="arial13grey">Matches are the best of 3 or 5 games, at the option of the competition organizer.<br /><br />
                a. Server Only Scores: The player who scores 9 points first wins the game, except that on the score reaching 8-all for the first time, the receiver chooses, before the next service, to continue that game either to 9 (known as "Set 1") or to 10 (known as "Set 2"). In the latter case the player who scores 2 more points wins the game. The receiver must clearly indicate this choice to the Marker, Referee and the opponent.<br /><br />
                b. PAR 11: Each game is point a rally scoring to 11. If the score in a game is tied at 10-10, a player must win by 2 clear points.<br /><br />
                c. PAR 15: Each game is point a rally scoring to 15. If the score in a game is tied at 14-14, a player must win by 2 clear points.<br /><br /></a>
                <a class="arial14grey"><b>3. How to win points</b><br /><br /></a>
                <a class="arial13grey">a. Server Only Scores: Only the server scores points. The server, on winning a rally, scores a point; the receiver, on winning a rally, becomes the server without a change of score.<br /><br />
                b. PAR 11 & PAR 15: Points can be scored by either player. When a player fails to serve or to return the ball, in accordance with the rules, the opponent wins the point. When the Receiver wins a point, they become the Server and add one to their score.<br /><br /></a>
                <a class="arial14grey"><b>4. Service</b><br /><br /></a>
                <a class="arial13grey">a. The right to serve is decided by the spin of a racket or coin. Thereafter the Server continues to serve until they lose the point; the opponent then becomes the Server.<br /><br />
                b. At the beginning of each game and each hand, the Server has the choice of serving from either service box and shall thereafter alternate for as long as they score points and remains the Server.<br /><br />
                c. The ball must be served direct on to the front wall between the service line and the out line, so that on its return, unless volleyed, its first bounce lands within the back quarter of the court opposite to the Server’s box.<br /><br />
                d. A service is a fault: (i) If at the time of striking the ball the Server fails to have at least one foot in contact with the floor within the service box, and no part of that foot touching the line surrounding the service box (called a foot-fault ).<br /><br /></a>
                <a class="arial14grey"><b>5. Play</b><br /><br /></a>
                <a class="arial13grey">After a good service has been delivered, the players return the ball alternately until one or other fails to make a good return, or the ball otherwise ceases to be in play, in accordance with the rules.<br /><br />
                NB: If any part of the ball touches the out line the ball is deemed out or if the ball hits the tin or the floor on its first bounce the ball is deemed down. Similarly, if the ball bounces on or before the short line from a service, the serve is deemed a fault.<br /><br /></a>
                <a class="arial14grey"><b>6. A Good Return</b><br /><br /></a>
                <a class="arial13grey">A return is good if the ball, before it has bounced twice on the floor, is returned by the striker on to the front wall above the tin, without touching the floor or any part of the striker’s body or clothing, provided the ball is not hit twice, down or out.<br /><br /></a>
                <a class="arial14grey"><b>7. Lets</b><br /><br /></a>
                <a class="arial13grey">A Let is an undecided stroke, and the service or rally in respect of which a Let is allowed, shall not count and the Server shall serve again from the same box.<br /><br />
                (a) A let may be allowed:<br /><br />
                (i) If the striker refrains from hitting the ball owing to a reasonable fear of injuring the opponent.<br /><br />
                (ii) If the striker, on the way to playing the ball incurs sufficient interference to impede their access to the ball when the striker would have otherwise played a good shot.<br /><br />
                (iii) If the Referee is asked to decide an appeal and is unable to do so.<br /><br />
                (b) A Let shall be allowed:<br /><br />
                (i) If the Receiver is not ready, and does not attempt to take the service.<br /><br />
                (ii) If the ball breaks during play.<br /><br />
                (iii) If an otherwise good return has been made, but the ball goes out on its first bounce.<br /><br />
                (iv) If the ball in play touches the receiver or anything carried or worn by the receiver whilst on the way to the side wall or back wall in anticipation that the ball would have reached the front wall and hence been a good return.<br /><br /></a>
                <a class="arial14grey"><b>8. How strokes (points) can be won</b><br /><br /></a>
                <a class="arial13grey">A player wins a stroke:<br /><br />
                (a) If the server fails to make a good serve (See point 4 on the Service).<br /><br />
                (b) If the opponent fails to make a good return of the ball in play.<br /><br />
                (c) If the ball in play touches the opponent or anything carried or worn by the opponent whilst on the way to the front wall.<br /><br />
                (d) If a player drops the racket, calls out or in any other way distracts the opponent, and the Referee considers that such occurrence has caused the opponent to lose the stroke.<br /><br />
                (e) If the ball hits the ceiling or any object outside of the court.<br /><br /></a>
                <a class="arial14grey"><b>9. Freedom to play the ball</b><br /><br /></a>
                <a class="arial13grey">After playing a ball, a player must make every effort to get out of the opponents way.<br /><br />
                That is:<br /><br />
                (i) A player must make every effort to give the opponent a fair view of the ball, so that it may be sighted adequately for the purpose of playing it.<br /><br />
                (ii) A player must make every effort not to interfere with, or crowd, the opponent in the latter’s attempt to get to, or play, the ball.<br /><br />
                (iii) A player must make every effort to allow the opponent, as far as the latter’s position permits, freedom to play the ball directly to the front wall, or to either side wall.<br /><br /></a>
                <a class="arial14grey"><b>10. Warm-Up</b><br /><br /></a>
                <a class="arial13grey">Immediately preceding the start of play, a period not exceeding five minutes shall be allowed on court for the purpose of warming-up.<br /><br /></a>
